// Get a quote by theme
export const getQuoteByTheme = (theme) => {
  const themed = quotes.filter(q => q.themes.includes(theme));
  if (themed.length === 0) return quotes[Math.floor(Math.random() * quotes.length)];
  return themed[Math.floor(Math.random() * themed.length)];
};

// Get a random quote
export const getRandomQuote = () => {
  return quotes[Math.floor(Math.random() * quotes.length)];
};

// Get quote of the day (deterministic based on date)
export const getQuoteOfTheDay = () => {
  const today = new Date();
  const dayOfYear = Math.floor((today - new Date(today.getFullYear(), 0, 0)) / (1000 * 60 * 60 * 24));
  return quotes[dayOfYear % quotes.length];
};

