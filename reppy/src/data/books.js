// Get a book by theme
export const getBookByTheme = (theme) => {
  const themed = books.filter(b => b.themes.includes(theme));
  if (themed.length === 0) return books[Math.floor(Math.random() * books.length)];
  return themed[Math.floor(Math.random() * themed.length)];
};

// Get a random book
export const getRandomBook = () => {
  return books[Math.floor(Math.random() * books.length)];
};

// Get book bite for session (deterministic based on session number)
export const getBookForSession = (sessionNumber) => {
  return books[(sessionNumber - 1) % books.length];
};

export default books;
